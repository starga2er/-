ans =0

def correction(i, j, tet):
    global N, M, nlist
    ans = 0
    for t in tet:
        if not( 0 <= t[0] +i < N and 0 <= t[1] +j < M):
            return -1
        ans += nlist[t[0]+i][t[1]+j]
    return ans


for i in range(N):
    for j in range(M):
        for t in tetromia:
            ans = max(ans, correction(i,j,t))
